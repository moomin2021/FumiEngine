#include "Stage.h"

Stage::Stage() :
	// モデル
	wallM_(nullptr),// -> 壁

	// オブジェクト
	wallsObj_{},

	// 当たり判定用
	wallsCol_{}
{}

Stage::~Stage() {
	delete wallM_;
	wallsObj_.clear();
	wallsCol_.clear();
}

